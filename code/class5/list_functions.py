listOfNumbers = [5, 22, 3, 45]

# There are lots of helpful list functions! Such as...
# - len <-- length
# - max <-- find biggest thing in list
# - min <-- find smallest thing in list
# - sum <-- add up all the things in the list
print("List: ", listOfNumbers)
print("Length of list: ", len(listOfNumbers) )
print("Biggest number:", max(listOfNumbers) )
print("Smallest number:", min(listOfNumbers) )
print("Total: ", sum(listOfNumbers))

# You can also sort your lists
print("List, sorted: ", sorted(listOfNumbers) )
